"""Persona eval driver: the measurement substrate for playbook optimization.

Drives a :class:`PlaybookAgent` through persona-scripted sessions and folds
each session's event log into metrics (completion, per-checkpoint turn
counts, slot accuracy against the persona's ground truth, repair and
degradation counts). The future ``superdialog optimize`` command consumes
these reports; the reflective optimizer itself lives elsewhere.
"""

from __future__ import annotations

from typing import Any, Callable, Protocol

from pydantic import BaseModel, Field

from .agent import PlaybookAgent
from .events import (
    AdvanceEvent,
    DegradedEvent,
    EventLog,
    SteeringNoteEvent,
    UtteranceEvent,
)
from .state import ConversationState

_PERSONA_SYSTEM = (
    "You are role-playing a caller. Traits: {traits}. Your goal: {goal}. "
    "Reply with ONLY the caller's next utterance, 1-2 sentences."
)
_TRANSCRIPT_WINDOW = 10  # transcript entries shown to the persona LLM


class PersonaSpec(BaseModel):
    """A simulated caller: who they are, what they want, what they convey."""

    name: str
    traits: str  # prose: "impatient, gives all details at once"
    goal: str  # "book a tee time in Pune tomorrow"
    max_turns: int = 12
    opening: str = "Hello"  # the persona's first utterance
    # What the persona KNOWS it conveyed; measured against extracted slots.
    ground_truth_slots: dict[str, Any] = Field(default_factory=dict)


class SessionMetrics(BaseModel):
    """Measurements folded from one persona-driven session."""

    persona: str
    completed: bool  # terminal checkpoint reached
    outcome: str | None
    turns: int  # user turns driven
    turns_per_checkpoint: dict[str, int]
    slot_accuracy: float  # extracted∩correct / ground truth; 1.0 if none
    # key -> (expected, extracted) for mismatched or missing slots
    slot_diffs: dict[str, tuple[Any, Any]]
    repair_count: int  # steering events kind=repair
    degraded_count: int  # DegradedEvent count
    event_log_jsonl: str  # full session log for replay/audit


class EvalReport(BaseModel):
    """Aggregate of persona sessions; the optimizer's objective surface."""

    sessions: list[SessionMetrics]

    @property
    def completion_rate(self) -> float:
        """Fraction of sessions that reached a terminal checkpoint."""
        if not self.sessions:
            return 0.0
        return sum(s.completed for s in self.sessions) / len(self.sessions)

    @property
    def mean_slot_accuracy(self) -> float:
        """Mean slot accuracy across sessions (0.0 when empty)."""
        if not self.sessions:
            return 0.0
        return sum(s.slot_accuracy for s in self.sessions) / len(self.sessions)


class SpeaksUser(Protocol):
    """Persona-LLM seam: anything that completes chat messages to text."""

    async def complete(self, messages: list[dict[str, str]], **kwargs: Any) -> str: ...


async def run_session(
    agent: PlaybookAgent, persona: PersonaSpec, user_llm: SpeaksUser
) -> SessionMetrics:
    """Drive one persona session against ``agent`` and measure it.

    The persona speaks first (``persona.opening``); each subsequent caller
    utterance is generated by ``user_llm`` from the live transcript. The
    session ends at a terminal checkpoint, on an empty persona reply, or
    after ``persona.max_turns`` user turns.
    """
    await agent.runtime.start()  # greeting/pass-through lands in the log
    user_text = persona.opening
    turns = 0
    while turns < persona.max_turns:
        await agent.turn(user_text)
        turns += 1
        if agent.runtime.state.ended or turns >= persona.max_turns:
            break
        messages = _persona_messages(persona, agent.runtime.state)
        user_text = (await user_llm.complete(messages)).strip()
        if not user_text:
            break
    return _measure(agent, persona, turns)


async def run_eval(
    playbook_factory: Callable[[], PlaybookAgent],
    personas: list[PersonaSpec],
    user_llm: SpeaksUser,
    n: int = 1,
) -> EvalReport:
    """Run each persona ``n`` times against fresh agents; aggregate metrics."""
    sessions = [
        await run_session(playbook_factory(), persona, user_llm)
        for persona in personas
        for _ in range(n)
    ]
    return EvalReport(sessions=sessions)


def _persona_messages(
    persona: PersonaSpec, state: ConversationState
) -> list[dict[str, str]]:
    """System prompt + recent transcript, roles flipped to the caller's view.

    The persona LLM speaks the caller, so prior caller utterances are its
    own assistant turns and agent speech is incoming user input.
    """
    system = _PERSONA_SYSTEM.format(traits=persona.traits, goal=persona.goal)
    messages = [{"role": "system", "content": system}]
    for entry in state.transcript[-_TRANSCRIPT_WINDOW:]:
        if entry.role == "system":
            continue
        role = "assistant" if entry.role == "user" else "user"
        messages.append({"role": role, "content": entry.text})
    return messages


def _measure(agent: PlaybookAgent, persona: PersonaSpec, turns: int) -> SessionMetrics:
    """Fold the finished session's log and state into metrics."""
    log = agent.runtime.log
    state = agent.runtime.state
    accuracy, diffs = _slot_accuracy(persona.ground_truth_slots, state)
    return SessionMetrics(
        persona=persona.name,
        completed=state.ended,
        outcome=state.outcome,
        turns=turns,
        turns_per_checkpoint=_turns_per_checkpoint(log),
        slot_accuracy=accuracy,
        slot_diffs=diffs,
        repair_count=sum(
            1
            for e in log.events
            if isinstance(e, SteeringNoteEvent) and e.kind == "repair"
        ),
        degraded_count=sum(1 for e in log.events if isinstance(e, DegradedEvent)),
        event_log_jsonl=log.to_jsonl(),
    )


def _turns_per_checkpoint(log: EventLog) -> dict[str, int]:
    """Count user utterances per checkpoint they happened IN.

    AdvanceEvents bound the windows; revisited checkpoints accumulate.
    """
    counts: dict[str, int] = {}
    current: str | None = None
    for e in log.events:
        if isinstance(e, AdvanceEvent):
            current = e.to_checkpoint
        elif isinstance(e, UtteranceEvent) and e.role == "user" and current:
            counts[current] = counts.get(current, 0) + 1
    return counts


def _slot_accuracy(
    expected: dict[str, Any], state: ConversationState
) -> tuple[float, dict[str, tuple[Any, Any]]]:
    """Compare ground truth against final slots, str()-normalized.

    Returns (accuracy, diffs) where diffs maps key -> (expected, extracted)
    for mismatched or missing slots. No ground truth scores 1.0.
    """
    if not expected:
        return 1.0, {}
    diffs: dict[str, tuple[Any, Any]] = {}
    correct = 0
    for key, want in expected.items():
        got = state.slot_value(key)
        if got is not None and str(want).lower() == str(got).lower():
            correct += 1
        else:
            diffs[key] = (want, got)
    return correct / len(expected), diffs


